fn main() {
    assert_eq!(format!("{:.4}", 14.544444), "14.5444");
    assert_eq!(format!("{:.3}", 14.544444), "14.544");
    assert_eq!(format!("{:.3}", 14.546744), "14.547");
    assert_eq!(format!("{:<8.3}", 14.544444), "14.544  ");
    assert_eq!(format!("{:^8.3}", 14.544444), " 14.544 ");
    assert_eq!(format!("{:-^8.3}", 14.544444), "-14.544-");
}
